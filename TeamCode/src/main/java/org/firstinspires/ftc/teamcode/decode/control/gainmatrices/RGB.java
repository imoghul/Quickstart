package org.firstinspires.ftc.teamcode.decode.control.gainmatrices;

public class RGB {
    public double red;
    public double green;
    public double blue;

    public RGB() {
        this(0.0, 0.0, 0.0);
    }

    public RGB(double red, double green, double blue) {
        this.red = red;
        this.green = green;
        this.blue = blue;
    }
}
